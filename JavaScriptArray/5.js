// Write a simple JavaScript program to join all elements of the following array
// into a string.
var myColor = ["Red", "Green", "White", "Black"];
// Joining with no separator
var joinedString1 = myColor.join(',');
console.log(joinedString1);
// Joining with a hyphen
var joinedString2 = myColor.join(' ');
console.log(joinedString2); 
// Joining with a space
var joinedString3 = myColor.join('+');
console.log(joinedString3);
